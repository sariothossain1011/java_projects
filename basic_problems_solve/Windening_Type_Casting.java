public class Windening_Type_Casting {
    public static void main(String[] args) {
        int myInt = 9;
        double myDouble = myInt; // automatic casting: int to double
        System.out.println("original int vlaue: " + myInt); // Outputs - 9
        System.out.println("int to double converted value: " + myDouble); // Outputs - 9.0

    }
}
